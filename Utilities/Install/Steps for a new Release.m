%% Steps for a new Release (my use only..)

% Local
% 1) Update gnctoolsver version, release notes and date
% 2) Rebuild MEX files
% 3) Run GNCTools_RunTests to run all unit tests
% 4) Run GNCTools_package to update Contents file

% Git
% 1) Commit the latest changes with the new version
% 2) Tag the release using the format "GNCTools_vX.XX_Released"
% 3) Push the changes to Github
% 4) Under GNCTools/Releases, draft a new release. Select the tag, title "GNCTools vX.XX".

% Website
% 1) Update GNCTools Wiki page with new features under news